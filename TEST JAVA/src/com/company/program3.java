
            // PROBLEM3 TO PRINT MULTTIPLICATION OF NUMBER

package com.company;

public class program3 {
    public static void main(String[] args) {
        int n = 10;
        for (int i = 1; i <= 10; i++) {
            System.out.printf("%d x %d = %d\n", n, i, n * i);
        }
    }
}
